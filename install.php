 * Fio Trans Cargo Website Installer
 * This installer will set up the database and configure the website
 */

// Prevent direct access after installation
if (file_exists('installed.lock')) {
    die('Website sudah terinstall. Hapus file "installed.lock" jika ingin menginstall ulang.');
}

$step = isset($_GET['step']) ? (int)$_GET['step'] : 1;
$error = '';
$success = '';

// Handle form submissions
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    switch ($step) {
        case 1:
            // Database configuration step
            $db_host = trim($_POST['db_host'] ?? '');
            $db_name = trim($_POST['db_name'] ?? '');
            $db_user = trim($_POST['db_user'] ?? '');
            $db_pass = $_POST['db_pass'] ?? '';
            
            if (empty($db_host) || empty($db_name) || empty($db_user)) {
                $error = 'Semua field database wajib diisi kecuali password.';
            } else {
                // Test database connection
                try {
                    $pdo = new PDO("mysql:host=$db_host", $db_user, $db_pass);
                    $pdo->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);
                    
                    // Create database if not exists
                    $pdo->exec("CREATE DATABASE IF NOT EXISTS `$db_name` CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci");
                    $pdo->exec("USE `$db_name`");
                    
                    // Save database config to separate file
                    $config_content = "<?php
// Database configuration - Generated by installer
define('DB_HOST', '$db_host');
define('DB_NAME', '$db_name');
define('DB_USER', '$db_user');
define('DB_PASS', '$db_pass');
?>";
                    
                    file_put_contents('backend/config/db_config.php', $config_content);
                    
                    // Execute database schema
                    $schema = file_get_contents('backend/database/schema.sql');
                    $statements = explode(';', $schema);
                    
                    foreach ($statements as $statement) {
                        $statement = trim($statement);
                        if (!empty($statement)) {
                            $pdo->exec($statement);
                        }
                    }
                    
                    $success = 'Database berhasil dikonfigurasi!';
                    $step = 2;
                    
                } catch (PDOException $e) {
                    $error = 'Error database: ' . $e->getMessage();
                }
            }
            break;
            
        case 2:
            // Site configuration step
            $site_title = trim($_POST['site_title'] ?? '');
            $site_description = trim($_POST['site_description'] ?? '');
            $admin_email = trim($_POST['admin_email'] ?? '');
            $admin_password = $_POST['admin_password'] ?? '';
            
            if (empty($site_title) || empty($admin_email) || empty($admin_password)) {
                $error = 'Semua field wajib diisi.';
            } else {
                try {
                    // Update site settings
                    include 'backend/config/database.php';
                    $database = new Database();
                    $db = $database->getConnection();
                    
                    if ($db) {
                        // Update settings
                        $stmt = $db->prepare("UPDATE settings SET setting_value = ? WHERE setting_key = ?");
                        $stmt->execute([$site_title, 'site_title']);
                        $stmt->execute([$site_description, 'site_description']);
                        
                        // Update admin user
                        $hashed_password = password_hash($admin_password, PASSWORD_DEFAULT);
                        $stmt = $db->prepare("UPDATE users SET email = ?, password = ? WHERE username = 'admin'");
                        $stmt->execute([$admin_email, $hashed_password]);
                        
                        // Create installation lock file
                        file_put_contents('installed.lock', date('Y-m-d H:i:s'));
                        
                        $success = 'Instalasi berhasil! Website Anda siap digunakan.';
                        $step = 3;
                    } else {
                        $error = 'Gagal terhubung ke database.';
                    }
                } catch (Exception $e) {
                    $error = 'Error: ' . $e->getMessage();
                }
            }
            break;
    }
}
